package com.paradise.think.ops3;

/**
 * @author Paradise
 */
public class AutoInc {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("++i = " + ++i);
        System.out.println("i++ = " + i++);
        System.out.println("i = " + i);
        System.out.println("--i = " + --i);
        System.out.println("i-- = " + i--);
        System.out.println("i = " + i);
    }
}
